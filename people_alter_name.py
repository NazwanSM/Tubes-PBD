# Hapus duplikasi dari daftar kanji_names dan hiragana_names
def generate_people_alter_names(df):
    # Pastikan panjang list cukup
    df_length = len(df)
    
    # Siapkan daftar nama alternatif yang cukup panjang
    kanji_subset = (kanji_names * ((df_length // len(kanji_names)) + 1))[:df_length]
    hiragana_subset = (hiragana_names * ((df_length // len(hiragana_names)) + 1))[:df_length]
    
    # Acak urutan nama alternatif
    random.shuffle(kanji_subset)
    random.shuffle(hiragana_subset)
    
    # Buat list untuk menyimpan data alternate names
    alternate_names_list = []
    
    # Loop melalui DataFrame asli
    for idx, row in df.iterrows():
        original_name = row['name']
        
        # Tambahkan nama kanji sebagai alternate name
        alternate_names_list.append({
            'name': original_name,
            'alternate_name': kanji_subset[idx]
        })
        
        # Tambahkan nama hiragana sebagai alternate name
        alternate_names_list.append({
            'name': original_name,
            'alternate_name': hiragana_subset[idx]
        })
    
    # Buat DataFrame dari list alternate names
    people_alter_names_df = pd.DataFrame(alternate_names_list)
    
    return people_alter_names_df

